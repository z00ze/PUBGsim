public class LogSwimEnd {
    // "character": {Character}, "swimDistance": float
    Character character;
    float swimDistance;

    public LogSwimEnd(Character character, float swimDistance) {
        this.character = character;
        this.swimDistance = swimDistance;
    }

    public Character getCharacter() {
        return character;
    }

    public float getSwimDistance() {
        return swimDistance;
    }
}
